public class Math_class {
    public static void main(String args[]){
        double x=3.14,y=-10;
        double z;

        z=Math.max(x, y);
        System.out.println(z);

        y=Math.abs(y);
        System.out.println(y);

        z=Math.round(x);
        System.out.println(z);

        z=Math.ceil(x);
        System.out.println(z);

        y=Math.sqrt(y);
        System.out.println(y);

    }
}
