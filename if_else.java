public class if_else {
    public static void main(String args[]){
        int age = 21;
        if(age<18){
            System.out.println("You are not an adult");
        }
        else if(age<21){
            System.out.println("You recently became an adult");
        } 
        else{
            System.out.println("You are an adult");
        }
    }
}
